// DOM要素の取得
const startBtn = document.getElementById("start-btn");
const backFromRegionBtn = document.getElementById("back-from-region");
const backFromPrefBtn = document.getElementById("back-from-pref");
const backFromCityBtn = document.getElementById("back-from-city");
const retryBtn = document.getElementById("retry-btn");
const copyBtn = document.getElementById("copy-btn");

const startScreen = document.getElementById("start-screen");
const regionScreen = document.getElementById("region-screen");
const prefectureScreen = document.getElementById("prefecture-screen");
const cityScreen = document.getElementById("city-screen");
const resultScreen = document.getElementById("result-screen");

const regionList = document.getElementById("region-list");
const prefectureList = document.getElementById("prefecture-list");
const cityList = document.getElementById("city-list");
const prefectureHeading = document.getElementById("prefecture-heading");
const cityHeading = document.getElementById("city-heading");
const resultInfo = document.getElementById("result-info");
const resultList = document.getElementById("result-list");

let currentRegion = "";
let currentPrefecture = "";

// 画面切り替え関数
const showScreen = (screenToShow) => {
  const allScreens = [startScreen, regionScreen, prefectureScreen, cityScreen, resultScreen];
  allScreens.forEach(screen => screen.classList.add("hidden"));
  screenToShow.classList.remove("hidden");
};

// 地域のボタンを生成する関数
const renderRegions = () => {
  regionList.innerHTML = "";
  Object.keys(data).forEach(region => {
    const button = document.createElement("button");
    button.className = "region-btn";
    button.textContent = region;
    button.addEventListener("click", () => {
      currentRegion = region;
      renderPrefectures(region);
      showScreen(prefectureScreen);
    });
    regionList.appendChild(button);
  });
};

// 都道府県のボタンを生成する関数
const renderPrefectures = (region) => {
  prefectureList.innerHTML = "";
  prefectureHeading.textContent = `${region}の都道府県を選んでください`;
  Object.keys(data[region]).forEach(prefecture => {
    const button = document.createElement("button");
    button.className = "prefecture-btn";
    button.textContent = prefecture;
    button.addEventListener("click", () => {
      currentPrefecture = prefecture;
      if (prefecture === "該当不明" || data[region][prefecture].length === 0) {
        renderResult(prefecture, []);
        showScreen(resultScreen);
      } else {
        renderCities(region, prefecture);
        showScreen(cityScreen);
      }
    });
    prefectureList.appendChild(button);
  });
};

// 市区町村のボタンを生成する関数
const renderCities = (region, prefecture) => {
  cityList.innerHTML = "";
  cityHeading.textContent = `${prefecture}の市区町村を選んでください`;
  const cities = data[region][prefecture];
  if (Array.isArray(cities)) {
    // ユーザーが提供したデータは市区町村の階層がないので、都道府県選択後すぐに結果を表示する
    renderResult(prefecture, cities);
    showScreen(resultScreen);
  } else {
    // 市区町村データがある場合
    Object.keys(cities).forEach(city => {
      const button = document.createElement("button");
      button.className = "city-btn";
      button.textContent = city;
      button.addEventListener("click", () => {
        const results = data[region][prefecture][city];
        renderResult(city, results);
        showScreen(resultScreen);
      });
      cityList.appendChild(button);
    });
  }
};

// 結果を表示する関数
const renderResult = (city, results) => {
  resultInfo.textContent = `${currentRegion} > ${currentPrefecture} > ${city}`;
  resultList.innerHTML = "";

  if (results.length > 0) {
    results.forEach(item => {
      const li = document.createElement("li");
      li.innerHTML = item;
      resultList.appendChild(li);
    });
  } else {
    const p = document.createElement("p");
    p.textContent = "該当する情報がありませんでした。";
    resultList.appendChild(p);
  }
};

